//Part one
let lowest, highest, password, letterStr, re, count;
let validCounter = 0;
let invalidCounter = 0;


for (let i = 0; i < input.length; i++){
    item = input[i];
  
    lowest = item.low;
    highest = item.max;
    password = item.pass;
    letterStr = item.letter;

    re = new RegExp(`${letterStr}`, 'g'); 
    count = (password.match(re) || []).length;

    if (count < lowest || count > highest){
        invalidCounter++;
    }else{
        validCounter++;
    }
}  

console.log("==== Part One ====");
console.log("Valid passwords: " +validCounter);
console.log("Invalid passwords: " +invalidCounter);
console.log("Total passwords: " +(validCounter+invalidCounter));

validCounter = 0;
invalidCounter = 0;

//Part two
let firstPos, lastPos;

for (let i = 0; i < input.length; i++){
    item = input[i];
  
    lowest = item.low-1;
    highest = item.max-1;
    password = item.pass;
    letterStr = item.letter;

    firstPos = password.substr(lowest,1);
    lastPos = password.substr(highest,1);

    if (firstPos == letterStr && lastPos != letterStr || lastPos == letterStr && firstPos != letterStr){
        validCounter++;
    }else{
        invalidCounter++;
    }
}  

console.log("\n==== Part Two ====");
console.log("Valid passwords: " +validCounter);
console.log("Invalid passwords: " +invalidCounter);
console.log("Total passwords: " +(validCounter+invalidCounter));
